// Test is video autoplay is supported
export default function supportsVideoAutoplay (callback) {
  if (typeof callback !== 'function') {
    return false
  }

  // Create video element to test autoplay
  let video = document.createElement('video')

  video.autoplay = true
  video.src = `data:video/mp4;base64,${testBase64Video}`
  video.load()

  // (EKL) Okay, what happens here is as follows:
  // - the loadeddata event instructs the video to start playing
  // - if autoplay is possible, the playing event is fired and the callback is called with true and erased
  // - after 200ms the loaddeddata event checks if the video is not playing and the callback is still available
  // - when the callback is not erased we know autoplay isn't possible so fire the callback with false
  // this seems to be the only reliable way to detect autoplay, even on IOS10
  video.addEventListener('loadeddata', () => {
    video.play()

    window.setTimeout(() => {
      if (video && video.paused && callback) {
        callback(false)
        callback = null
        video = null
      }
    }, 200)
  }, false)

  video.addEventListener('playing', () => {
    callback(true)
    callback = null
    video = null
  }, false)
}
